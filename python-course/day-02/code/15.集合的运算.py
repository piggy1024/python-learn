# 15.集合的运算.py

# 创建两个集合
s = {1,2,3,4,5}
s1 = {3,4,5,6,7}

# 运算不会影响原来的集合

# & 交集运算
result = s & s1  #{3,4,5}
print(result)


# \ 并集运算
result = s | s1 #{1,2,3,4,5,6,7}
print(result)

# - 差集运算
result = s - s1  #{1,2}
print(result)  

# ^ 异或集合   获取只在某一个集合出现过的元素
print(s ^ s1)  #{1,2,6,7}

