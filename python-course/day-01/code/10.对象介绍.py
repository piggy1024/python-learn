#10.对象介绍
# id 标识
# 	> id用来标识对象的唯一性，可通过id（）函数来查看对象的id，由解析器生成
# 	> id就是对象的内存地址
# 	> 对象一旦创建，它的id永远不会改变
# type
# 	> 标识当前对象所属的类型
# 	> 类型决定对象有什么功能
# 	> Python是一门强类型语言，对象一旦创建类型不再改变
# value
# 	> 值就是对象中国存储的具体的类型
# 	> 对于有些对象值是可以改变的
# 	> 对于有些对象值是可以改变的
## 变量和对象
    # 对象并没有直接存储到变量中，在oython中变量更像是给对象起了一个别名
    # 变量中存储的不是对象的值，而是对象的id（内存地址）
    #  使用变量实际就是通过对象id在查找对象